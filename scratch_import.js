async function run() {
  const { data: children, error: childErr } = await supabase.from('children').select('*');
  if (childErr) {
    console.error(childErr);
    return;
  }
  
  const jingyeol = children.find(c => c.name === '진결');
  const jinsol = children.find(c => c.name === '진솔');

  if (jingyeol) {
    console.log(`Inserting ${jingyeolData.length} records for 진결...`);
    for (const record of jingyeolData) {
      // Upsert or insert without overwriting height if it exists
      const { data: existing } = await supabase
        .from('growth_records')
        .select('*')
        .eq('child_id', jingyeol.id)
        .eq('record_date', record.date);

      if (existing && existing.length > 0) {
        await supabase
          .from('growth_records')
          .update({ weight: record.weight })
          .eq('child_id', jingyeol.id)
          .eq('record_date', record.date);
      } else {
        await supabase
          .from('growth_records')
          .insert({
            child_id: jingyeol.id,
            record_date: record.date,
            weight: record.weight,
            height: null
          });
      }
    }
  }

  if (jinsol) {
    console.log(`Inserting ${jinsolData.length} records for 진솔...`);
    for (const record of jinsolData) {
      const { data: existing } = await supabase
        .from('growth_records')
        .select('*')
        .eq('child_id', jinsol.id)
        .eq('record_date', record.date);

      if (existing && existing.length > 0) {
        await supabase
          .from('growth_records')
          .update({ weight: record.weight })
          .eq('child_id', jinsol.id)
          .eq('record_date', record.date);
      } else {
        await supabase
          .from('growth_records')
          .insert({
            child_id: jinsol.id,
            record_date: record.date,
            weight: record.weight,
            height: null
          });
      }
    }
  }
  
  console.log("Done inserting all records!");
}

run();
